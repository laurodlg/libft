#include "libft.h"

void	ft_putnbr(int n)
{
	char nbr;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = (n * -1);
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			n = n % 10;
		}
		nbr = n + '0';
		write(1, &nbr, 1);
	}
}
