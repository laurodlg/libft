#include "libft.h"

char	*ft_itoa(int n)
{
	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	if (n == 0)
		return (ft_strdup("0"));
	if (n > 0)
		return (ft_itoapos(n));
	else
		return (ft_itoaneg(n));
}
