#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		pos;
	long	r;

	i = 0;
	pos = 1;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
		str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		pos = str[i] == '-' ? -1 : 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = ((r * 10) + (str[i] - '0'));
		i++;
	}
	return (r * pos);
}
