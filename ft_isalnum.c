#include <stdio.h>

int	ft_isalnum(int c)
{
	while (c >= 48 && c <= 57)
	{
		return (1);
	}
	while (c >= 65 && c <= 90)
	{
		return (1);
	}
	while (c >= 97 && c <= 122)
		return (1);
	return (0);
}
